private void setDurationAndSetText() {
    try {
        // Get the current checkout date
        Date checkoutDate = new Date(System.currentTimeMillis());
         
        // Get the date string from jFormattedTextFieldRegDateEdit
        String dateString = jFormatedTextFieldRegDateEdit.getText().trim();

        // Parse the date string to a Date object using SimpleDateFormat
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
        Date registerDate = dateFormat.parse(dateString);

        // Calculate the difference in milliseconds between checkout date and register date
        long diffInMillies = checkoutDate.getTime() - registerDate.getTime();

        // Convert the difference from milliseconds to days
        int days = (int) TimeUnit.DAYS.convert(diffInMillies, TimeUnit.MILLISECONDS) + 1;

        // Set the duration to jTextFieldDays
        jTextFieldDays.setText(Integer.toString(days));
        TotalRoomPrice = roomPrice * days;
        jTextFieldRoomTotalPrice.setText(String.format("Rs.%.2f", TotalRoomPrice));
    } catch (ParseException e) {
        e.printStackTrace(); // Handle the parsing exception appropriately
        // Show an error message or perform error handling as needed
    }
    
     
}
private double calculateTotalPrice(double roomPrice, int days, double TotalRoomPrice) {
    try {
        // Get the text from the formatted text field
        String chargesText = jFormattedTextFieldCharges.getText();

        // Remove any non-numeric characters (except '.')
        chargesText = chargesText.replaceAll("[^\\d.]", "");

        // Check if the text is not empty
        if (!chargesText.isEmpty()) {
            // Parse the charges into a double
            Charges = Double.parseDouble(chargesText);
        } else {
            // If the text is empty, set Charges to 0
            Charges = 0.0;
        }

        // Calculate the total price
        double totalPrice = Charges + TotalRoomPrice;
        return totalPrice;
    } catch (NumberFormatException e) {
        // Handle the NumberFormatException (e.g., display an error message)
        e.printStackTrace();
        return -1; // Return a default value or handle the error as needed
    }
}





