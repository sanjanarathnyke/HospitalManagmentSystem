    private void setTextFromData(ArrayList<String> data) {
        // Ensure data is not null and has enough elements
        if (data != null && data.size() >= 5) {
            jLabelRoomTotalPrice.setText( data.get(0));
            jLabelName.setText(  data.get(1));
            jLabelID.setText(  data.get(2));
            jLabelPhoneNo.setText( data.get(3));
            jLabelRoomType.setText("Room Type: " + data.get(4));
            jFormattedTextFieldDate.setText(dateFormat.format(new java.sql.Date(System.currentTimeMillis())));
            jFormattedTextFieldChargesPrice.setText("Rs: " + data.get(5));
           jFormattedTextFieldTotal.setText(" Total: " + data.get(6));
            
        } else {
            System.err.println("Invalid data array.");
        }
        
        
    }
    
