 public void printPanel(JPanel panelToPrint) {
    MyPrintable printable = new MyPrintable(panelToPrint);
    PrinterJob job = PrinterJob.getPrinterJob();
    job.setPrintable(printable);

    if (job.printDialog()) {
        try {
            job.print();
        } catch (PrinterException ex) {
            // Handle printing exception
            ex.printStackTrace();
        }
    }
}

 
 
    
